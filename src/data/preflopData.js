// Calculate range percentage
export function calcRangePercent(grid) {
  let total = 0;
  let count = 0;
  for (let r = 0; r < 13; r++) {
    for (let c = 0; c < 13; c++) {
      const freq = grid[r][c];
      // Pocket pairs = 6 combos, suited = 4 combos, offsuit = 12 combos
      let combos;
      if (r === c) combos = 6;
      else if (c > r) combos = 4; // suited
      else combos = 12; // offsuit
      total += combos;
      count += combos * (freq / 100);
    }
  }
  return ((count / total) * 100).toFixed(1);
}
