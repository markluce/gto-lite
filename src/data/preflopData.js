// Value 0-100 = raise frequency percentage

// Generate hand name from grid position
export function getHandName(row, col) {
  if (row === col) return ranks[row] + ranks[col]; // Pocket pair
  if (col > row) return ranks[row] + ranks[col] + "s"; // Suited (upper triangle)
  return ranks[col] + ranks[row] + "o"; // Offsuit (lower triangle)
}

